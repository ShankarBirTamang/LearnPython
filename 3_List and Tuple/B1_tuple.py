# Tuples are immutable  i.e. elements of tuple cant be changed. No replace, insert ,delete ,sort ,append operation in tuple
# It is also a group of elements containing parenthesis ,() instead of square brackets [].
# It is also zero based indexing

animal = ('dog','cat','rat')
print(animal)
print(animal[0])

# nested tuple
tuple1 = ((1,2.3),(3,4.4))
print(tuple1)

# empty tuple
tuple2 = ()
print(tuple2)